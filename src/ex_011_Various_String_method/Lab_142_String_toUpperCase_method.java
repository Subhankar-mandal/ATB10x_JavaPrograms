package ex_011_Various_String_method;

public class Lab_142_String_toUpperCase_method {
    /** toUpperCase() method: In Java, String toUpperCase() method of String class is used to convert all
     *                        the lowercase characters to uppercase in a given string.
     * Syntax: toUpperCase();                              */
    public static void main(String[] args) {
        String s1 = "computer science";
        System.out.println(s1.toUpperCase());
        String s2 = "i lOVE cs";
        System.out.println(s2.toUpperCase());

    }
}
