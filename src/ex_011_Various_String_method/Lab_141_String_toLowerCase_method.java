package ex_011_Various_String_method;

public class Lab_141_String_toLowerCase_method {
    /** String toLowerCase() method: In Java, toLoweCase() method is used to convert all the letters from
     *                               uppercase to lowercase in a given string.
     * Syntax: toLowerCase();             */
    public static void main(String[] args) {
        String s1 = "CompUTER Science ";
        s1 = s1.toLowerCase();
        System.out.println(s1);
        String s3 = "S.M is the abbreviation of Subhankar Mandal.";
        System.out.println(s3.toLowerCase());

    }
}
