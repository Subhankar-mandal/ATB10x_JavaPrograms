package ex_03_Java_Literal;

public class Lab_029_Literal {
    /** Literal: Any constant value which can be assigned to the variable is called literal.*/
    public static void main(String[] args) {

        int age = 35;           //  35 is integer literal.

        double salary = 43529.89;        // 43529.89 is floating-point literal.

        System.out.printf("age is %d, salary is %f", age,salary);

    }
}
