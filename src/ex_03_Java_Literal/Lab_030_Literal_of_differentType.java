package ex_03_Java_Literal;

public class Lab_030_Literal_of_differentType {
    /** Literal of different types: 1. Integral literal,  2. Floating-point literal
     *                              3. Character literal, 4. String literal
     *                              5. Boolean literal                */

    public static void main(String[] args) {
        int age = 35;                   // integral literal.

        float PI = 3.14f;               // floating-point literal
        char c = 'A';                   // character literal
        String name = "Subhankar";      // string literal
        boolean isMarried = false;      // boolean literal

        System.out.println(isMarried);
    }
}

