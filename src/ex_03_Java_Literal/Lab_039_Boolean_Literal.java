package ex_03_Java_Literal;

public class Lab_039_Boolean_Literal {
    /** Boolean literal: In Java, a boolean literal represents a constant boolean value, which can
     *                   be either true or false.It is used for boolean dta-type.
     *                   Boolean literals are true and false.These values are case-insensitive. */

    public static void main(String[] args) {
        boolean isTrue = true;
        boolean isFalse = false;
        if(!isFalse){
            System.out.println("This statement is True.");
        }
        else{
            System.out.println("This statement is False.");
        }

    }
}
