package ex_08_Loop_Statements;

public class Lab_094_For_loop {
    /** For Loop: It is very widely used loop statement in every programming language.
     * Syntax: 1. for (loop counter initialization; condition; increment/decrement){
     *                 statement 1;
     *                 statement 2;
     *                 statement n;
     *            }                                                       */

    public static void main(String[] args) {
        int i = 0;
        for (i =0; i<=5; i++){
//            System.out.println(i);
            System.out.println(i+"\tPrint value of i until loop is terminating.");
        }

    }
}
