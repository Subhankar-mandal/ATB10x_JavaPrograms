package ex_08_Loop_Statements;

public class Lab_100_For_loop_example_06 {
    /** Counter variable: Counter variable can be of different data-types. */
    public static void main(String[] args) {
        for (float i = 2.0f; i <10.1 ; i++) {
            System.out.println(i);

        }
        for (long l = 1; l<= 10; ++l){
            System.out.println(l);
        }
    }
}
