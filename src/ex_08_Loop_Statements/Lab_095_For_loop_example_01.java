package ex_08_Loop_Statements;

public class Lab_095_For_loop_example_01 {
    public static void main(String[] args) {

        for (int God = 1; God <= 10; ++God) {
            System.out.println(God);       // 11 times, print 0 to 10
        }

        for (int _1 = 0; _1 <= 10; _1++) { // 11 times
            System.out.println(_1); // 0, to 10
        }
    }
}
