package ex_02_JavaBasics;

public class Lab_018_byte_Data_type {
    /** Byte is a primitive data-type which is integral type.
     * Size: 8 bits, 1 Byte. Range: -2^7 = -128 to 2^7-1 = 127
     * */
    public static void main(String[] args) {
        byte b = 10;
        System.out.println(b+127);
    }
}
