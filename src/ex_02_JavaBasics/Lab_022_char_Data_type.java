package ex_02_JavaBasics;

public class Lab_022_char_Data_type {
    /** Char is a primitive data-type which is single character type.
     * denoted within single quotation mark as 'A', 'f' etc.
     * Java supports Unicode character sets that includes different languages
     * like Chinese, Greek, Arabic etc.so it needs 2 Bytes of memory.
     *  Size: 16 bits, 2 Bytes. Range: -2^15 to 2^15-1  */
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);
    }
}
